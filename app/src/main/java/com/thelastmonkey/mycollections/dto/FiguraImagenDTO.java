public class FiguraImagenDTO {
    String idFiguraImagen;
    String idFigura;
    String idImagen;
    String fecha;

    public String getIdFiguraImagen() {
        return idFiguraImagen;
    }

    public void setIdFiguraImagen(String idFiguraImagen) {
        this.idFiguraImagen = idFiguraImagen;
    }

    public String getIdFigura() {
        return idFigura;
    }

    public void setIdFigura(String idFigura) {
        this.idFigura = idFigura;
    }

    public String getIdImagen() {
        return idImagen;
    }

    public void setIdImagen(String idImagen) {
        this.idImagen = idImagen;
    }

    public String getFecha() {
        return fecha;
    }

    public void setFecha(String fecha) {
        this.fecha = fecha;
    }
}
